    private void IniciarTabla(){
        
        // limpia los datos de los Label y los datos relacionados a la actualizacion de los datos e inhabilita y habilitar botones
this.jLabelId.setText("Id");
this.jnombre.setText("");
grabarCambios.setEnabled(false);
jBotonAgregar.setEnabled(true);

    DefaultTableModel dfm = new DefaultTableModel();
        tabla = this.jTabla;
        tabla.setModel(dfm);
        
        // agrega los datos al index de la tabla 
        dfm.setColumnIdentifiers(new Object[]{"id","Canal","Estado"});
        Conexion cn = new Conexion();
      rs = cn.SeleccionarCanal();
       try{
           // se recorre rs donde estan los resultados de la busqueda de los datos de la tabla canales
       while(rs.next()){
           String activo ="";
           if (rs.getInt("CAN_ESTADO")==1) {
               activo = "activo";
                       } else {
               activo = "no activo";};
           // se agrega la fila con los datos de la columna         
       dfm.addRow(new Object[]{rs.getInt("CAN_ID_CANAL"),rs.getString("CAN_NOMBRE"),activo});
       }
       
       } catch(Exception e){
       System.out.println("Error Revisar funcion TableModel" + e);
       }
    }
    
    
    
    
    

